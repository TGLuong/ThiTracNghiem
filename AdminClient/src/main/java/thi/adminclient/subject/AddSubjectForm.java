    private void btn_addSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addSubjectActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            sw = new StringWriter();
            subject = new Subject(txt_id_subject.getText(),txt_name_subject.getText());
            marshaller.marshal(subject, sw);
            http = (HttpURLConnection)getURL.openConnection();
            http.setRequestMethod("POST");
            http.setDoOutput(true);
            http.setRequestProperty("Content-Type", "application/xml;charset=UTF-8");
            out = new DataOutputStream((http.getOutputStream()));
            out.write(sw.toString().getBytes("UTF-8"));
            out.flush();
            out.close();
            http.getInputStream();
            if(http.getResponseCode()==200)
            {
                JOptionPane.showMessageDialog(null, "Thêm Thành công");
            }
        } catch (MalformedURLException ex) {
            Logger.getLogger(SubjectManager.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(SubjectManager.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JAXBException ex) {
            Logger.getLogger(SubjectManager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_addSubjectActionPerformed

