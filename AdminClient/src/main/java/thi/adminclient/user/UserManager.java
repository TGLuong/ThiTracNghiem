    private void btn_getUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_getUserActionPerformed
        String mssv=JOptionPane.showInputDialog(null, "Nhập mã sinh viên");
        if(mssv.length()>0)
        {
        try {
            // TODO add your handling code here:
            URL getURL = new URL("http://localhost:8989/thitracnghiem/api/admin/user/info?id_user="+mssv);
            HttpURLConnection http = (HttpURLConnection)getURL.openConnection();
            http.setRequestMethod("GET");
            JAXBContext jabc = JAXBContext.newInstance(Users.class);
            Unmarshaller unmarshaller = jabc.createUnmarshaller();
            User user = (User)unmarshaller.unmarshal(http.getInputStream());
            mode.getDataVector().removeAllElements();
            mode.addRow(new Object[]{user.getId_user(),user.getName_user(),user.getBirthday(),user.getId_class(),user.getId_department()});
            
        } catch (MalformedURLException ex) {
            Logger.getLogger(UserManager.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(UserManager.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JAXBException ex) {
            Logger.getLogger(UserManager.class.getName()).log(Level.SEVERE, null, ex);
        }        

        }
    }//GEN-LAST:event_btn_getUserActionPerformed

    private void btn_getListUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_getListUserActionPerformed
        // TODO add your handling code here:

        try {
            // TODO add your handling code here:
            URL getURL = new URL("http://localhost:8989/thitracnghiem/api/admin/user");
            HttpURLConnection http = (HttpURLConnection)getURL.openConnection();
            http.setRequestMethod("GET");
            JAXBContext jabc = JAXBContext.newInstance(Users.class);
            Unmarshaller unmarshaller = jabc.createUnmarshaller();
            Users listUser = (Users)unmarshaller.unmarshal(http.getInputStream());
            mode.getDataVector().removeAllElements();
            for(User user:listUser.getList())
            {
                mode.addRow(new Object[]{user.getId_user(),user.getName_user(),user.getBirthday(),user.getId_class(),user.getId_department()});
            } 
        } catch (MalformedURLException ex) {
            Logger.getLogger(UserManager.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(UserManager.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JAXBException ex) {
            Logger.getLogger(UserManager.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }//GEN-LAST:event_btn_getListUserActionPerformed

