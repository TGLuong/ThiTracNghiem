    private void btn_deleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteUserActionPerformed
        // TODO add your handling code here:
        String mssv=JOptionPane.showInputDialog(null, "Nhập mã sinh viên");
        if(mssv.length()>0)
        {
        try {
            // TODO add your handling code here:
            URL getURL = new URL("http://localhost:8989/thitracnghiem/api/admin/user?id_user="+mssv);
            HttpURLConnection http = (HttpURLConnection)getURL.openConnection();
            http.setRequestMethod("DELETE");
            http.setDoOutput(true);
            http.setRequestProperty("Content-Type", "text/plain;charset=UTF-8");
            DataOutputStream out = new DataOutputStream(http.getOutputStream());
            out.writeBytes("");
            out.flush();
            out.close();
            http.getInputStream();
            this.btn_getListUserActionPerformed(evt);
        } catch (MalformedURLException ex) {
            Logger.getLogger(UserManager.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(UserManager.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btn_deleteUserActionPerformed

